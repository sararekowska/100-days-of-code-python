def calculate_score(cards_list):
    if cards_list[0] in blackjack and cards_list[1] in blackjack:
        score = 0
    else:
        score = sum(cards_list)
        if score > 21 and 11 in cards_list:
            cards_list.remove(11)
            cards_list.append(1)
    return score

